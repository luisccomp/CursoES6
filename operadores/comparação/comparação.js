// Igual (==)
// Retorna verdadeiro caso os operandos sejam iguais. 3 == var1.
"3" == var1
"3" == 3

// Não igual (!=)
// Retorna verdadeiro caso os operandos sejam não iguais. 3 != var1.
"3" != var2

// Estritamente igual (===)
// Retorna verdadeiro se os operandos são iguais e possuem o mesmo tipo.
3 === var1

// Estritamente não igual (!==)
// Retorna verdadeiro se os operandos são iguais e e/ou não são do mesmo tipo.
var1 !== "3"
3 !== "3"

// Maior que (>)
// Retorna verdadeiro caso o operando da esquerda seja maior que o da direita.
var1 > var2
"12" > 2

// Maior ou igual (>=)
// Retorna verdadeiro caso o operando da esquerda seja maior ou igual que o da direita.
var1 >= var2
var1 >= 3

// Menor que (<)
// Retorna verdadeiro caso o operando da esquerda seja menor que o da direita.
var1 < var2
"12" < "2"

// Menor ou igual (<=)
// Retorna verdadeiro caso o operando da esquerda.
var1 <= var2
var2 <= 5
